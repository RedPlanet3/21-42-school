int	ft_str_is_numeric(char *str)
{
	int ch;
	int i;

	ch = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if ('0' > str[i] || str[i] > '9')
			return (0);
		i++;
	}
	return (ch);
}
