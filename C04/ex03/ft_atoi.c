int	ft_atoi(char *str)
{
	int min;
	int num;

	min = 0;
	num = 0;
	while (*str < '0' || *str > '9')
	{
		if (*str == 45)
			min++;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = (*str - 48) + 10 * num;
		str++;
	}
	if (min % 2 == 1)
		return (num * -1);
	else
		return (num);
}
