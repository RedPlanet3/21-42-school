int	ft_sqrt(int nb)
{
	int i;
	int ch;

	i = 1;
	ch = 1;
	if (nb > 0)
	{
		while ((nb - i) > 0)
		{
			nb = nb - i;
			i = i + 2;
			ch++;
		}
	}
	if (nb - i == 0)
		return (ch);
	else
		return (0);
}
